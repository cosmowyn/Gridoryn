def _iso_day(base_day: date, offset_days: int = 0) -> str:
    return (base_day + timedelta(days=int(offset_days))).isoformat()


def _iso_stamp(
    base_day: date,
    offset_days: int = 0,
    *,
    hour: int = 9,
    minute: int = 0,
) -> str:
    target = base_day + timedelta(days=int(offset_days))
    return f"{target.isoformat()} {hour:02d}:{minute:02d}:00"


def _phase_map(start_id: int) -> dict[str, int]:
    return {
        "intake": start_id,
        "planning": start_id + 1,
        "execution": start_id + 2,
        "testing": start_id + 3,
        "approval": start_id + 4,
        "closure": start_id + 5,
    }


def _phase_rows(
    project_task_id: int,
    phase_ids: dict[str, int],
    stamp: str,
) -> list[dict]:
    ordered = [
        ("Intake", phase_ids["intake"]),
        ("Planning", phase_ids["planning"]),
        ("Execution", phase_ids["execution"]),
        ("Testing", phase_ids["testing"]),
        ("Approval", phase_ids["approval"]),
        ("Closure", phase_ids["closure"]),
    ]
    return [
        {
            "id": phase_id,
            "project_task_id": int(project_task_id),
            "name": str(name),
            "sort_order": index,
            "created_at": stamp,
            "updated_at": stamp,
        }
        for index, (name, phase_id) in enumerate(ordered, start=1)
    ]


