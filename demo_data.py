def populate_demo_database(db: Database, today: date | None = None) -> dict:
    if db.fetch_tasks():
        raise ValueError("Demo data can only be loaded into an empty database.")

    payload = build_demo_payload(today=today)
    existing_columns = {str(col.get("name") or "") for col in db.fetch_custom_columns()}
    for column in payload.get("custom_columns") or []:
        name = str(column.get("name") or "").strip()
        if not name or name in existing_columns:
            continue
        db.add_custom_column(
            name,
            str(column.get("col_type") or "text"),
            list(column.get("list_values") or []),
        )
        existing_columns.add(name)

    report: ImportReport = import_payload(None, payload, db)
    attachment_count = _create_demo_assets(db)

    rows = db.fetch_tasks()
    archived = sum(1 for row in rows if str(row.get("archived_at") or "").strip())
    recurring = sum(1 for row in rows if row.get("recurrence"))
    project_profiles = db.list_project_profiles()
    project_ids = [int(row["task_id"]) for row in project_profiles]
    milestone_count = sum(
        len(db.fetch_project_milestones(project_id))
        for project_id in project_ids
    )
    deliverable_count = sum(
        len(db.fetch_project_deliverables(project_id))
        for project_id in project_ids
    )
    register_count = sum(
        len(db.fetch_project_register_entries(project_id))
        for project_id in project_ids
    )
    phase_count = sum(
        len(db.fetch_project_phases(project_id))
        for project_id in project_ids
    )
    dependency_count = sum(
        len(db.fetch_project_dependencies(project_id))
        for project_id in project_ids
    )
    top_level_projects = len(project_profiles)
    return {
        "imported_tasks": int(report.imported_tasks or 0),
        "created_columns": int(report.created_columns or 0),
        "task_count": len(rows),
        "archived_count": archived,
        "saved_view_count": len(db.list_saved_filter_views()),
        "template_count": len(db.list_templates()),
        "recurring_count": recurring,
        "project_count": top_level_projects,
        "milestone_count": milestone_count,
        "deliverable_count": deliverable_count,
        "register_count": register_count,
        "phase_count": phase_count,
        "pm_dependency_count": dependency_count,
        "attachment_count": attachment_count,
        "custom_column_count": len(db.fetch_custom_columns()),
    }


def create_demo_workspace(
    manager: WorkspaceProfileManager,
    *,
    today: date | None = None,
    name: str = DEMO_WORKSPACE_NAME,
) -> dict:
    record = manager.create_workspace(
        str(name or DEMO_WORKSPACE_NAME),
        inherit_current_state=True,
    )
    db_path = str(record.get("db_path") or "")
    db = Database(db_path)
    try:
        summary = populate_demo_database(db, today=today)
    except Exception:
        try:
            db.conn.close()
        except Exception:
            pass
        try:
            manager.remove_workspace(str(record.get("id") or ""))
        except Exception:
            pass
        try:
            Path(db_path).unlink(missing_ok=True)
        except Exception:
            pass
        raise
    finally:
        try:
            db.conn.close()
        except Exception:
            pass
    return {"workspace": record, "summary": summary}
